package first_colloquium_ppj_training.Task3;

public class Task304 {
    public static void main(String args[]){
        System.out.println("iterały logiczne: "+true+" "+false);
        //liczba całkowita w postaci:
        //całkowita, dziesiętna
        System.out.println(21);
        //oktalna zapisując dajemy przedrostek 0
        System.out.println(025);
        //heksadecymalna zapisujac dajemy przedrostek 0x
        System.out.println(0x15);
        //binarna zapisujac dajemy przedrostek 0b
        System.out.println(0b10101);

        //liczby rzeczywiste
        System.out.println(3.14);

        //literaly znakowe
        System.out.println("zwykły łańcuch znaków");
    }
}
